def gpt_with_error_handling(content):
    print(f'gpt_with_error_handling content: {content}')
    try:
        response = openai.ChatCompletion.create(
            model="gpt-4o-mini",  # Replace with your custom GPT instance
            messages=[{"role": "user", "content": content}],
            max_tokens=10000,
            temperature=0.5
        )
        return response.choices[0].message['content'].strip()
    
    except openai.OpenAIError as e:
        print(f"An error occurred: {e}")
        return None

def split_novel(text, max_size):
    res_list = []
    if len(text) > 5000:
        index = 0
        while index <= len(text):
            if index + max_size < len(text):
                split_text = text[index:index + max_size]
                index = index + max_size
                supplement_index = index
                while split_text[-1] != '\n' and supplement_index < len(text):
                    split_text += text[supplement_index]
                    supplement_index += 1
                    index += 1
            else:
                split_text = text[index:]
                index = index + max_size
            res_list.append(split_text)
        return res_list
    else:
        return [text]
    
